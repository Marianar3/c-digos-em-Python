largura = float(input("Digite a largura da parede em metros: ")) #essa variavel pede a largura da parede
altura = float(input("Digite a altura da parede em metros: ")) #essa variavel pede a altura da parede
area = largura * altura #essa variavel calcula a area
litros = area / 2 # essa variavel calcula a quantidade de litros necessarios
print("A área da parede é:", area, "m²") #esse comando mostra o resultado da area total da parde
print("Você precisará de", litros, "litros de tinta para pintá-la.") #esse comando mostra a quantidade de litros para pintar

