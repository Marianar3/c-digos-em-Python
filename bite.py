gb = float(input("Digite o tamanho em gigabyte (gb): ")) # essa variavel pede ao usuário um número em GB
mb = gb * 1024 # conversão (1 GB = 1024 MB)
kb = mb * 1024 # conversão (1 MB = 1024 KB)
print("Valor em Megabyte (MB):", mb) # esse comando de print mostra o resultado
print("Valor em Kilobyte (KB):", kb) # esse comando de print tbm mostra o resultado